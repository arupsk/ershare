package com.Andriod.ER.com;

public class ReadWriteUserDetails {
    public String firstName,lastName,email,country,password;

    public ReadWriteUserDetails(String firstName, String lastName, String email, String country, String password) {
        this.firstName = firstName;
        this.lastName = lastName;
        this.email = email;
        this.country = country;
        this.password = password;
    }
}
